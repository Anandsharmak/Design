package CacheData;



public class CacheData {
	Integer index;
	String name;
	public CacheData(Integer index, String name) {
		super();
		this.index = index;
		this.name = name;
	}
	public Integer getIndex() {
		return index;
	}
	public void setIndex(Integer index) {
		this.index = index;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
